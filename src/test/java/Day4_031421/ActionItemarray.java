package Day4_031421;

public class ActionItemarray {
    public static void main(String[] args) {
        String[] cities;

        cities= new String[4];
        cities[0] = "Brooklyn";
        cities[1] = "Queens";
        cities[2] = "Manhattan";
        cities[3] = "Staten";

        for (int i=0;i< cities.length;i++) {
            if (cities[i] == "Brooklyn") {
                System.out.println("The name of the city is " + cities[i]);
            } else if (cities[i] == "Manhattan") {
                System.out.println("The name of the city is " + cities[i]);
            }

        }










    }//end of main

}//end of java
